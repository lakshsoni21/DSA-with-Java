import java.util.*;

public class loops{
    public static void main(String[] args){
        //Q1
        for(int x=0; x<11; x++){
            System.out.println(x);
        }

        for(int i=5; i < 10; i++){
            System.out.println("hello world");
        }
        //2 ki table with for loop
        for(int z=2; z < 11; z=z+2){
            System.out.println(z);
        }

        for(int f=1; f<11; f=f+2){
            System.out.println("Hello World");
        }
    }
}