import java.util.*;

public class whileloop{
    public static void main(String[] args){
        int i = 1;
        while(i<11){
            System.out.println(i);
            System.out.println("Hello World");
            i = i+1;
        }

        int z = 1;
        do{
            System.out.println(z);
            z++;
        }while(z<6);
    }
}